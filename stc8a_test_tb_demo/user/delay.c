#include "delay.h"


/******************************************************************************/
// 函数名称： 
// 输入参数： 
// 输出参数： 
// 函数功能： 
/******************************************************************************/
void Delay100ms()		//@24.000MHz
{
	unsigned char i, j, k;

	_nop_();
	_nop_();
	i = 13;
	j = 45;
	k = 214;
	do
	{
		do
		{
			while (--k);
		} while (--j);
	} while (--i);
}



/******************************************************************************/
// 函数名称： 
// 输入参数： 
